# Variables in Python
# A variable is a container for a value, which can be of various data types such as integers, floats, strings, lists, etc.
# In Python, you don't need to declare a variable before using it. You can simply assign a value to a variable using the equals sign (=).
# Here are some examples of how to create and use variables in Python:

# Integer variable
age = 25
print("Age:", age)
# Float variable
height = 5.9
print("Height:", height)
# String variable
name = "John"
print("Name:", name)
# List variable
fruits = ["apple", "banana", "cherry"]
# Tuple variable
coordinates = (10.0, 20.0)
# Dictionary variable
person = {"name": "Alice", "age": 30}
# Set variable
unique_numbers = {1, 2, 3, 4, 5}
print("Fruits:", fruits)
# Boolean variable
is_student = True
print("Is Student:", is_student)
# You can also change the value of a variable by assigning a new value to it:
age = 26
print("Updated Age:", age)
# You can use variables in expressions and functions:
year_of_birth = 2024 - age
print("Year of Birth:", year_of_birth)
# Variables can also be used in string formatting:
greeting = f"Hello, my name is {name} and I am {age} years old."
print(greeting)
# In Python, variable names must start with a letter or an underscore (_) and can be followed by letters, digits, or underscores. Variable names are case-sensitive, meaning that "age" and
# "Age" are considered different variables.
# Avoid using reserved keywords as variable names, such as "if", "for", "while", etc.
# This is a simple example of how to use variables in Python.
# You can run this code in a Python environment to see the output.
# Feel free to modify the values and experiment with different data types!
# Output:
# Age: 25
# Height: 5.9
# Name: John
# Fruits: ['apple', 'banana', 'cherry']
# Is Student: True
# Updated Age: 26
# Year of Birth: 1998
# Hello, my name is John and I am 26 years old.

# PRIMITIVE DATA TYPES
# Primitive data types are the most basic data types available in a programming language. They are used to represent simple values and are not composed of other data types. In Python, the following are the most common primitive data types:
# 1. Integer (int): Represents whole numbers, both positive and negative, without decimal points.
age = 30
print("Age:", age)
# 2. Float (float): Represents real numbers, including decimal points.
height = 5.9
print("Height:", height)
# 3. String (str): Represents a sequence of characters, enclosed in single or double quotes.
name = "Alice"
print("Name:", name)
# 4. Boolean (bool): Represents one of two values: True or False.
is_student = True
print("Is Student:", is_student)
# 5. NoneType (None): Represents the absence of a value or a null value.
data = None
print("Data:", data)
# These primitive data types are the building blocks for more complex data structures and are used extensively in programming to store and manipulate data.
# You can run this code in a Python environment to see the output.
# Feel free to modify the values and experiment with different data types!
# Output:
# Age: 30
# Height: 5.9
# Name: Alice
# Is Student: True
